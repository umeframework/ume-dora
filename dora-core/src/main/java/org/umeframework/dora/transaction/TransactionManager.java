package org.umeframework.dora.transaction;

/**
 * TransactionManager
 * 
 * @author Yue MA
 * 
 */
public interface TransactionManager {

    /**
     * begin
     */
    void begin(int propagation);
    /**
     * begin
     */
    void begin();

    /**
     * commit
     */
    void commit();

    /**
     * roll back
     */
    void rollback();

    /**
     * setPropagation
     * 
     * @param propagation
     */
    void setPropagation(
            int propagation);

    /**
     * getPropagation
     * 
     * @return
     */
    int getPropagation();
}
